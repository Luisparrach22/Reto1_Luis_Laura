package reto;

public class funciones2 {

	public static int multiplicar (int a, int b) {
		return a*b;
	}
	
	public static double dividir (int a, int b) {
		return a/b;
	}
	
	public static boolean positivo (int n) {
		if (n>0) {
			return true;	
		}
		else {
			return false;
		}
	}
	
	public static boolean negativo (int n) {
		if (n<=0) {
			return true;
		}
		
		else {
			return false;
		}
	}
}



