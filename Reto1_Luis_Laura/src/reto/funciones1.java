package reto;

public class funciones1 {

    public int sumar(int a, int b) {
        return a + b;
    }

    public int restar(int a, int b) {
        return a - b;
    }

    public boolean esPar(int n) {
        return n % 2 == 0;
    }

    public boolean esImpar(int n) {
        return n % 2 != 0;
    }
}
