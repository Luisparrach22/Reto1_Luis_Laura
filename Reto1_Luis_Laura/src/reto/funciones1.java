package reto;

public class funciones1 {

    public static int sumar(int a, int b) {
        return a + b;
    }

    public static int restar(int a, int b) {
        return a - b;
    }

    public static boolean espar(int n) {
        return n % 2 == 0;
    }

    public static boolean esimpar(int n) {
        return n % 2 != 0;
    }
}
